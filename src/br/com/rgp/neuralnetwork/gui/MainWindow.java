    private boolean isValidInputs() {
        return (!jFormattedTextField1.getText().isEmpty() &&
                !jFormattedTextField2.getText().isEmpty() &&
                !jFormattedTextField3.getText().isEmpty() &&
                !jFormattedTextField4.getText().isEmpty());
    }
    
    private LisFlower activeNeuronFlower(double[] outputs) {
        int setosa = LisFlower.SETOSA.ordinal();
        int versicolor = LisFlower.VERSICOLOR.ordinal();
        int virginia = LisFlower.VIRGINIA.ordinal();
        
        if (outputs[setosa] > outputs[versicolor] && outputs[setosa] > outputs[virginia]) {
            return LisFlower.SETOSA;
        } else if (outputs[versicolor] > outputs[virginia]) {
            return LisFlower.VERSICOLOR;
        }
        return LisFlower.VIRGINIA;
    }
    
