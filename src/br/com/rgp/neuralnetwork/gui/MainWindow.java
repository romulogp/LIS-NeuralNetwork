    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        NumberFormat formatInstance = DecimalFormat.getInstance(new Locale("pt", "BR"));
        formatInstance.setMaximumFractionDigits(2);
        double[] inputs = new double[4];
        try {
            inputs[0] = formatInstance.parse(jFormattedTextField1.getText()).doubleValue();
            inputs[1] = formatInstance.parse(jFormattedTextField2.getText()).doubleValue();
            inputs[2] = formatInstance.parse(jFormattedTextField3.getText()).doubleValue();
            inputs[3] = formatInstance.parse(jFormattedTextField4.getText()).doubleValue();
        } catch (ParseException pe) {
            JOptionPane.showMessageDialog(null, "Os dados informados estão incorretos.");            
        }
        
        NeuralNetwork perceptron = MultiLayerPerceptron.createFromFile("lis.nnet"); 
        perceptron.setInput(inputs);
        perceptron.calculate();
        double[] outputs = perceptron.getOutput();
        LisFlower activeSpecie = activeNeuronFlower(outputs);
        LocalDateTime now = LocalDateTime.now(ZoneId.systemDefault());
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");now.format(dtf);
        
        StringBuilder builder = new StringBuilder();
        builder.append(jTextAreaResultado.getText());
        builder.append(executionTime > 0 ? "\n\n" : "")
                .append("[").append(now.format(dtf)).append("]: Execução de testes. Teste ID: ").append(executionTime + 1)
                .append("\n")
                .append("Entradas: ").append(Arrays.toString(inputs))
                .append("\n")
                .append("Saídas: ").append(Arrays.toString(outputs))
                .append("\n")
                .append("Espécie: ").append(activeSpecie.getDescricao())
                .append("\n")
                .append("Confiabilidade: ").append(String.format("%.6f", outputs[activeSpecie.ordinal()] * 100)).append("%");
        
        jTextAreaResultado.setText(builder.toString());
        
        executionTime++;
    }//GEN-LAST:event_jButton1ActionPerformed

